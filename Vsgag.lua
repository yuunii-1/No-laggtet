Spawner.SpawnSeed("Candy Blossom") -- Spawns Candy Blossom
Spawner.SpawnEgg("Night Egg") -- Spawns Night Egg
Spawner.Spin("Sunflower") -- Spins Sunflower
Spawner.Load() -- Loads the Default UI
